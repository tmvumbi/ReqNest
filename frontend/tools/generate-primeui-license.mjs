import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const frontendRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const localEnvironmentPath = join(frontendRoot, '.env.local');
const generatedModulePath = join(frontendRoot, 'src', 'app', 'primeui-license.generated.ts');

function readLocalLicense() {
  if (!existsSync(localEnvironmentPath)) {
    return undefined;
  }

  const line = readFileSync(localEnvironmentPath, 'utf8')
    .split(/\r?\n/)
    .find((candidate) => candidate.trim().startsWith('PRIMEUI_LICENSE_KEY='));

  if (!line) {
    return undefined;
  }

  const value = line.slice(line.indexOf('=') + 1).trim();
  const isQuoted =
    value.length >= 2 &&
    ((value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'")));

  return isQuoted ? value.slice(1, -1) : value;
}

const licenseKey = process.env.PRIMEUI_LICENSE_KEY?.trim() || readLocalLicense()?.trim();
const generatedModule = `// Generated by tools/generate-primeui-license.mjs. Do not edit or commit.\nexport const PRIMEUI_LICENSE_KEY: string | undefined = ${JSON.stringify(licenseKey || undefined)};\n`;

writeFileSync(generatedModulePath, generatedModule, { encoding: 'utf8', mode: 0o600 });

console.log(
  licenseKey
    ? 'PrimeUI license configuration generated.'
    : 'PrimeUI license is not configured; set PRIMEUI_LICENSE_KEY or frontend/.env.local.',
);
